/**
 * @route   GET /api/github/user
 * @desc    Get GitHub user info
 * @access  Private
 */
router.get(
  '/user',
  authenticateToken,
  asyncHandler(async (req, res) => {
    const { githubToken } = req.query;

    if (!githubToken) {
      throw new APIError('GitHub token is required', 400);
    }

    try {
      const response = await axios.get('https://api.github.com/user', {
        headers: {
          Authorization: `token ${githubToken}`,
          Accept: 'application/vnd.github.v3+json',
        },
      });

      res.json({
        success: true,
        data: {
          username: response.data.login,
          name: response.data.name,
          avatar: response.data.avatar_url,
          publicRepos: response.data.public_repos,
        },
      });
    } catch (error) {
      if (error.response && error.response.status === 401) {
        throw new APIError('Invalid GitHub token', 401);
      }
      throw new APIError('Failed to fetch GitHub user', 500);
    }
  })
);

module.exports = router;
